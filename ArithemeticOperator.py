#Arithmetic Operators in Python

num1 = 2
num2 = 6

#Addition Operator '+'
AddOp = num1 + num2
print(AddOp)

#Substraction Operator '-'
SubOp =num1 - num2
print(SubOp)

#Multiplication Operator '*'
MulOp = num1 * num2
print(MulOp)

#Division Operator '/'
DivOp = num2 / num1
print(DivOp)

#Modulus Operator '%'
ModOp = num2 % num1
print(ModOp)

#Exponentation Operator '**'
ExpOp = num1 ** num2
print(ExpOp)

#Floor Division Operator ' // '
FloOp = num1 // num2
print(FloOp)