#Comparision Operator in Python

num1 = 5
num2 = 10

#Equal Op
EqualOp = (num1 == num2)
print(EqualOp)

#Not Equal
NotEqOp = (num1 != num2)
print(NotEqOp)

#Greater Than
GrtThanOp = (num1 > num2)
print(GrtThanOp)

#Less Than
LessThanOp = (num1 < num2) 
print(LessThanOp)

#Greater Then Or Equal to
GrtEqOp = (num1 >= num2)
print(GrtEqOp)

#Less Than Equal to
LessThenEqOp = (num1 <= num2)
print(LessThenEqOp)